ระบบส่องสว่าง ภายในบ้าน
    ต้องมีระบบแสงสว่างภายในบ้านครบถ้วน
    มีการตรวจระดับแสดงสว่างให้พอเหมาะแก่การอยู่อาศัยในจุดต่างๆ ดังนี้
        - ส่วนอ่านหนังสือ
        - ส่วนทำงาน 
        - ส่วนใช้เครื่องคอมพิวเตอร์
        - ส่วนดูทีวี
        - ส่วนล้างจาน 
        - ส่วนห้องครัว 
    ต้องมีการจัดเก็บข้อมูลค่าแสงสว่างที่เหมาะสมของส่วนต่างๆ ไว้ในแฟ้มเพื่อใช้อ้างอิง โดยต้องมีการระบุถึงแหล่งอ้างอิงด้วย
    อ่างล้างจาน ต้องมีแสงสว่างพอเพียง 

ระบบส่องสว่างภายนอกบ้าน 
    ต้องมีระบบส่องสว่างในเวลากลางคืน เมื่อเดินผ่านจะต้องมีแสงไฟพอมองเห็นได้ 
    รอบบ้าน ต้องมีระบบไฟที่สามารถมองเห็นได้ในระยะไกล หรือสว่างเมื่อเข้าใกล้
    การเดินรอบบ้านเวลากลางคืน ต้องสามารถ ทำได้โดยไม่ต้องถือไฟฉายไป และมองเห็นได้และดูปลอดภัย 
    บริเวณราวตากผ้าต้องมีแสดงสว่างเพียงพอเมื่อตากผ้า โดยอาจติดไฟถาวร หรือไฟสว่างเมื่อเดินผ่านก็ได้
    ไฟโรงรถ ต้องมีแสงสว่างพอเพียงเมื่อเปิดใช้ 
    ส่วนบริเวณเก็บเครื่องมือช่าง ต้องมีแสงสว่างพอเพียง เมื่อเปิดใช้ สามารถมองเห็นเครื่องมือช่างได้อย่างชัดเจน 
    บริเวณโรงรถ เมื่อมาถึงบ้านตอนดึก ต้องมีไฟเห็นหน้าบ้านได้ เมื่อเดินเข้าใกล้หรือเปิดไว้ตลอดเวลา  

เครื่องซักผ้า
    ต้องตรวจดูว่าท่อระบายน้ำไม่ตัน 
    ต้องมี สายกราวด์ติดตั้งเรียบร้อย 

อ่างล้างจาน
    ต้องตรวจดูท่อน้ำเป็นระยะ ทุก 2 ปี ว่ามีท่อรั่วหรือไม่
    หากบริเวณอ่างล้างจานมี สีไม่สวยงาม ต้องทาสีเพิ่มและทำให้สวยงาม 

สายกราวด์ 
    ต้องมีการค้นหาข้อมูลเก็บไว้อ้างอิงพิมพ์เก็บไว้ในแฟ้ม 
    ต้องทำให้ถูกต้อง โดยมีอุปกรณ์ที่ต้องต่อสายกราวด์ได้แก่ 
        - เครื่องซักผ้า
        - เครื่องทำน้ำอุ่น
        - เตาอบ 
        - เตาอบไมโครเวฟ 
        - ตู้เย็น  

